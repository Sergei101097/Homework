numbers = int(input('Введите цифру от 1 до 10: '))

if numbers == 1:
    print('I')
elif numbers == 2:
    print('II')
elif numbers == 3:
    print('III')
elif numbers == 4:
    print('IV')
elif numbers == 5:
    print('V')
elif numbers == 6:
    print('VI')
elif numbers == 7:
    print('VII')
elif numbers == 8:
    print('VIII')
elif numbers == 9:
    print('IX')
elif numbers == 10:
    print('X')
else:
    print('Увы ошибка, введите цифры от 1 до 10')